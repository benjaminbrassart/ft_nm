#include <stdint.h>

uint16_t swap_u16(uint16_t n)
{
	return ((n << 8) & 0xFF00) | ((n >> 8) & 0x00FF);
}

int16_t swap_i16(int16_t n)
{
	return (int16_t)swap_u16((uint16_t)n);
}

uint32_t swap_u32(uint32_t n)
{
	return ((n << 24) & 0xFF000000) | ((n << 8) & 0x00FF0000) |
	       ((n >> 8) & 0x0000FF00) | ((n >> 24) & 0x000000FF);
}

int32_t swap_i32(int32_t n)
{
	return (int32_t)swap_u32((uint32_t)n);
}

uint64_t swap_u64(uint64_t n)
{
	return ((n << 56) & 0xFF00000000000000) |
	       ((n << 40) & 0x00FF000000000000) |
	       ((n << 24) & 0x0000FF0000000000) |
	       ((n << 8) & 0x000000FF00000000) |
	       ((n >> 8) & 0x00000000FF000000) |
	       ((n >> 24) & 0x0000000000FF0000) |
	       ((n >> 40) & 0x000000000000FF00) |
	       ((n >> 56) & 0x00000000000000FF);
}

int64_t swap_i64(int64_t n)
{
	return (int64_t)swap_u64((uint64_t)n);
}
